Node* solve(int a[],int n,int &preindex,int min,int max) {
    if(preindex>n) return;
    int key = a[preindex];
    Node*root;
    if(key>min&&key<max){
       root = newNode(key);
       preindex++; 
    }
    if(preindex<n){
        root->left = solve(a,n,preindex,min,key);
    }
    if(preindex<n){
        root->right = solve(a,n,preindex,key,max);
    }
    return root;
}

